/**
 * Writes assets/js/auth-config.js from environment variables at build time.
 * Netlify: Site settings → Environment variables — see assignments below.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const landingRoot = path.join(__dirname, '..');
const outPath = path.join(landingRoot, 'assets', 'js', 'auth-config.js');

const domain = (
  process.env.AUTH0_DOMAIN ||
  process.env.SYNTRIX_AUTH0_DOMAIN ||
  ''
).trim();
const clientId = (
  process.env.AUTH0_CLIENT_ID ||
  process.env.SYNTRIX_AUTH0_CLIENT_ID ||
  ''
).trim();
const audience = (
  process.env.AUTH0_AUDIENCE ||
  process.env.SYNTRIX_AUTH0_AUDIENCE ||
  ''
).trim();
const apiBase = (
  process.env.SYNTRIX_API_BASE ||
  'https://api.syntrix.solutions'
).trim();

const body = `// Auto-generated by scripts/write-auth-config.mjs (Netlify build or local env).
// For local dev without this script, edit the strings below by hand.
window.SYNTRIX_API_BASE = ${JSON.stringify(apiBase)};
window.SYNTRIX_AUTH0_DOMAIN = ${JSON.stringify(domain)};
window.SYNTRIX_AUTH0_CLIENT_ID = ${JSON.stringify(clientId)};
window.SYNTRIX_AUTH0_AUDIENCE = ${JSON.stringify(audience)};
`;

fs.writeFileSync(outPath, body, 'utf8');
console.log('auth-config.js written:', {
  SYNTRIX_API_BASE: apiBase,
  domain: domain ? '[set]' : '[empty]',
  clientId: clientId ? '[set]' : '[empty]',
  audience: audience ? '[set]' : '[empty]',
});
